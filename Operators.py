# arithmetic operators
num1 = 10
num2 = 2
sum = num1 + num2
print(float(sum))

sub = num1 - num2
print(sub)

mul = num1 * num2
print(mul)

div = num1 / num2
print(int(div))

rem = (10 % 3)
print(rem)

power = (5 ** 2)
print(power)

# comparison operators
print(3 <= 3)
print(3 != 2)

# logical operators
print(3 < 2 or 5 < 4)
print(2 < 3 or 5 < 4)

print(3 < 4 and 5 < 4)
print(3 < 4 or 5 < 6)

# membership operators
list1 = [1,2,3,4,5]
print(1 in list1)

# bitwise operators
x = 10 #1010
y = 5 #0101

print(x | y)
print(x & y)
